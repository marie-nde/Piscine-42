#include "doop.h"

int		addition(int val1, int val2)
{
	return (val1 + val2);
}

int		soustraction(int val1, int val2)
{
	return (val1 - val2);
}

int		division(int val1, int val2)
{
	return (val1 / val2);
}

int		modulo(int val1, int val2)
{
	return (val1 % val2);
}

int		multiplication(int val1, int val2)
{
	return (val1 * val2);
}
