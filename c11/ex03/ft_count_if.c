int		ft_count_if(char **tab, int length, int (*f)(char*))
{
	int i;
	int compt;

	i = 0;
	compt = 0;
	while (i < length)
	{
		if ((*f)(tab[i]) != 0)
			compt++;
		i++;
	}
	return (compt);
}
